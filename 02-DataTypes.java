 class Variables {
    public static void main(String[]args){
        int a = 2;
        float b = 4.1f;  //You have to add f in the end for float else it will give 
                         // copilation error.
        double c = 4.688686;
        char d = 'A';    // char will Store only charecters
         String g = "Surya";
         long h = 83453235409l;
         boolean isValid = false;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(g);
        System.out.println(h);
        System.out.println(isValid);        
       
    }
}


// Output:
// 2
// 4.1
// 4.688686
// A
// Surya
// 83453235409
// false